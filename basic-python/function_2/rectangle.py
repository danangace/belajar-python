def area():
    length = float(input("Masukkan panjang: "))
    width = float(input("Masukkan lebar: "))
    area = length * width
    print(f'Luas Persegi Panjang adalah: {area}')

def perimeter():
    length = float(input("Masukkan panjang: "))
    width = float(input("Masukkan lebar: "))
    perimeter = (2 * length)+(2 * width)
    print(f'Keliling Persegi Panjang adalah: {perimeter}')